    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        // TODO add your handling code here:
        //System.out.println("Hola");
         //this.lbl_Mensaje.setText("Hola");
        cliente.setNit(txt_nit.getText());
        cliente.setNombres(txt_nombres.getText());
        cliente.setApellidos(txt_apellidos.getText());
        cliente.setDireccion(txt_direccion.getText());
        cliente.setTelefono(txt_NumeroTel.getText());
        cliente.setFecha_de_Nacimiento(txt_FechadeNacimiento.getText());
        
       
        cliente.crearArrayYllenarlo();
        tbl_modelo.addRow(cliente.crearArrayYllenarlo());
        tbl_Clientes.setModel(tbl_modelo);
    }//GEN-LAST:event_btn_GuardarActionPerformed

    private void btn_RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RemoveActionPerformed
        // TODO add your handling code here:
        
        String borrar = txt_Delet.getText();
        int borrarConvertido = Integer.parseInt(borrar);
        
        tbl_modelo.removeRow(borrarConvertido - 1);
        
    }//GEN-LAST:event_btn_RemoveActionPerformed

